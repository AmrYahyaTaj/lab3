public class Q1 {
    public static void main(String[] args) {
        int n=1,m=-1;
        if(n<-m)
        {System.out.println(n);}
        else {System.out.println(m);}
        if(-n>=m)
        {System.out.println(n);}
        else {System.out.println(m);}
        double x=0,y=1;
        if(Math.abs(x-y)<1)
        {System.out.println(x);}
        else {System.out.println(y);}
        x=Math.sqrt(2);
        y=2;
        if((x*x)==y)
        {System.out.println(x);}
        else {System.out.println(y);}
    }
}
